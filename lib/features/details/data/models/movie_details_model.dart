/// A data model for a spoken language.
@HiveType(typeId: 6)
@JsonSerializable()
class SpokenLanguage extends HiveObject {
  /// The English name of the language.
  @HiveField(0)
  @JsonKey(name: 'english_name')
  final String? englishName;

  /// The ISO 639-1 code for the language.
  @HiveField(1)
  @JsonKey(name: 'iso_639_1')
  final String? iso639_1;

  /// The native name of the language.
  @HiveField(2)
  final String? name;

  /// Creates a [SpokenLanguage] instance.
  SpokenLanguage({this.englishName, this.iso639_1, this.name});

  /// Creates a [SpokenLanguage] from a JSON map.
  factory SpokenLanguage.fromJson(Map<String, dynamic> json) =>
      _$SpokenLanguageFromJson(json);

  /// Converts this [SpokenLanguage] to a JSON map.
  Map<String, dynamic> toJson() => _$SpokenLanguageToJson(this);
}
